<?php

namespace Drupal\charts_google\Settings\Google;

class ChartArea
{
    private $width;

    /**
     * @return mixed
     */
    public function getWidth()
    {
        return $this->width;
    }

    /**
     * @param mixed $width
     */
    public function setWidth($width)
    {
        $this->width = $width;
    }

}